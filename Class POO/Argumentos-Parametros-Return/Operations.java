package ArgumentosYParametros;

public class Operations {

    public float Suma(float a, float b) {
        float suma = a + b;
        return suma;
    }

    public float Resta(float a, float b) {
        float resta = a - b;
        return resta;
    }
    
    public float Multiplicacion(float a, float b) {
        float mult = a * b;
        return mult;
    }
    
    public float Division(float a, float b) {
        float div = a / b;
        return div;
    }
}
